    private void btnMemberAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMemberAddActionPerformed
        String id = txtMemberID.getText();
        String name = txtMemberName.getText();
        String book = txtMemberBook.getText();
        String date = txtMemberDays.getText();
        if (id.isEmpty() || name.isEmpty() || isNumber(name) || book.isEmpty() || isNumber(book) || date.isEmpty() || !isNumber(date)) {
            if (id.isEmpty()) {
                lblErrorMemberID.setText("Please Enter ID");
            } else {
                lblErrorMemberID.setText("");
            }
            if (name.isEmpty()) {
                lblErrorName.setText("Please Enter Name");
            } else if (isNumber(name)) {
                lblErrorName.setText("Please Enter Text Only");
            } else {
                lblErrorName.setText("");
            }
            if (book.isEmpty()) {
                lblErrorMemberBook.setText("Please Enter Book");
            } else if (isNumber(book)) {
                lblErrorMemberBook.setText("Please Enter Text Only");
            } else {
                lblErrorMemberBook.setText("");
            }
            if (date.isEmpty()) {
                lblErrorMemberDays.setText("Please Enter Days");
            } else if (!isNumber(date)) {
                lblErrorMemberDays.setText("Please Enter Number");
            } else {
                lblErrorMemberDays.setText("");
            }
        } else {
            lblErrorMemberID.setText("");
            lblErrorMemberDays.setText("");
            lblErrorMemberBook.setText("");
            lblErrorName.setText("");
            Member mb = new Member(id, name, book, date);
            String chk = mbM.checkBook(mb.getBook());
            if (chk == null) {
                JOptionPane.showMessageDialog(AddMember, "Cannot find book!");
            } else if (mbM.addMember(mb)) {
                JOptionPane.showMessageDialog(AddMember, "Successful!");
            } else {
                JOptionPane.showMessageDialog(AddMember, "Return book!");
            }
            refreshTableMem();
            txtMemberID.setEnabled(true);
            txtMemberName.setEnabled(true);
        }
        checkUpdate();
    }//GEN-LAST:event_btnMemberAddActionPerformed

