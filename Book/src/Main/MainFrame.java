                Book bk = bkM.findBookByID(id);
                txtBookIDUpdate.setText(bk.getID());
                txtTitleUpdate.setText(bk.getTitle());
                txtCategoryUpdate.setText(bk.getCategory());
                txtAuthorUpdate.setText(bk.getAuthor());
