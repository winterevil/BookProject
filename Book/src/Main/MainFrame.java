    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String id = txtBookIDUpdate.getText();
        String title = txtTitleUpdate.getText();
        String category = txtCategoryUpdate.getText();
        String author = txtAuthorUpdate.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("MMM-dd-yyyy");
        Date d = jdcDateUpdate.getDate();

        if (title.isEmpty() || isNumber(title) || category.isEmpty() || isNumber(category) || author.isEmpty() || isNumber(author) || d == null) {
            if (title.isEmpty()) {
                lblErrorTitleUpdate.setText("Please Enter Title");
            } else if (isNumber(title)) {
                lblErrorTitleUpdate.setText("Please Enter Text Only");
            } else {
                lblErrorTitleUpdate.setText("");
            }
            if (category.isEmpty()) {
                lblErrorCategoryUpdate.setText("Please Enter Category");
            } else if (isNumber(category)) {
                lblErrorCategoryUpdate.setText("Please Enter Text Only");
            } else {
                lblErrorCategoryUpdate.setText("");
            }
            if (author.isEmpty()) {
                lblErrorAuthorUpdate.setText("Please Enter Author");
            } else if (isNumber(author)) {
                lblErrorAuthorUpdate.setText("Please Enter Text Only");
            } else {
                lblErrorAuthorUpdate.setText("");
            }
            if (d == null) {
                lblErrorDateUpdate.setText("Please choose the date");
            } else {
                lblErrorDateUpdate.setText("");

            }
        } else {
            String date = sdf.format(jdcDateUpdate.getDate());
            lblErrorDateUpdate.setText("");
            lblErrorAuthorUpdate.setText("");
            lblErrorCategoryUpdate.setText("");
            lblErrorTitleUpdate.setText("");
            Book bk = new Book(id, title, category, author, date);
            bkM.editBook(bk, id);
            JOptionPane.showMessageDialog(UpdateBook, "Update successfull!");
            refreshTable();
        }
        checkUpdate();
    }//GEN-LAST:event_btnUpdateActionPerformed

