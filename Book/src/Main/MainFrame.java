    private void btnBookAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookAddActionPerformed
        // TODO add your handling code here:
        String id = txtBookIDAdd.getText();
        String title = txtTitleAdd.getText();
        String category = txtCategoryAdd.getText();
        String author = txtAuthorAdd.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("MMM-dd-yyyy");
        Date d = jdcDateAdd.getDate();

        if (id.isEmpty() || bkM.findBookByID(id) != null || title.isEmpty() || isNumber(title) || category.isEmpty() || isNumber(category) || author.isEmpty() || isNumber(author) || d == null) {
            if (id.isEmpty()) {
                lblErrorBookIDAdd.setText("Please Enter ID");
            } else if (bkM.findBookByID(id) != null) {
                lblErrorBookIDAdd.setText("ID Already Exists");
            } else {
                lblErrorBookIDAdd.setText("");
            }
            if (title.isEmpty()) {
                lblErrorTitleAdd.setText("Please Enter Title");
            } else if (isNumber(title)) {
                lblErrorTitleAdd.setText("Please Enter Text Only");
            } else {
                lblErrorTitleAdd.setText("");
            }
            if (category.isEmpty()) {
                lblErrorCategoryAdd.setText("Please Enter Category");
            } else if (isNumber(category)) {
                lblErrorCategoryAdd.setText("Please Enter Text Only");
            } else {
                lblErrorCategoryAdd.setText("");
            }
            if (author.isEmpty()) {
                lblErrorAuthorAdd.setText("Please Enter Author");
            } else if (isNumber(author)) {
                lblErrorAuthorAdd.setText("Please Enter Text Only");
            } else {
                lblErrorAuthorAdd.setText("");
            }
            if (d == null) {
                lblErrorDateAdd.setText("Please Choose Date");
            } else {
                lblErrorDateAdd.setText("");
            }
        } else {
            String date = sdf.format(jdcDateAdd.getDate());
            lblErrorBookIDAdd.setText("");
            lblErrorDateAdd.setText("");
            lblErrorAuthorAdd.setText("");
            lblErrorCategoryAdd.setText("");
            lblErrorTitleAdd.setText("");
            Book bk = new Book(id, title, category, author, date);
            bkM.addBook(bk);
            JOptionPane.showMessageDialog(AddBook, "Successful!");
        }
        checkUpdate();
    }//GEN-LAST:event_btnBookAddActionPerformed

